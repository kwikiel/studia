package Cwiczenia2;

public class AllLiterals {
    public static void main(String[] args) {
        // Literały logiczne 
        System.out.println(true);
        System.out.println(false);

        // Tej samej liczby całkowitej
        System.out.println(2137);
        System.out.println(04131); 
        System.out.println(0x859);
        System.out.println(0b100001011001);

        // Liczb rzeczywistych
        System.out.println(21.37);
        System.out.println(21.37);

        System.out.println("Zażółć gęśią jaźń");
        System.out.println("xD ==> ( ͡° ͜ʖ ͡°) ");

    }
}
